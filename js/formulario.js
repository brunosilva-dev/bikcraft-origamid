const formulario = document.querySelector("form");

function formularioEnviado(resposta) {
  if (resposta.ok) {
    formulario.innerHTML =
      "<p class='font-2-l' style='grid-column: 1/-1; padding: 1rem; border-radius: 4px; background: #f7f7f7;>Mensagem enviada, em breve entraremos em contato!";
  } else {
    formulario.innerHTML =
      "<p>Erro no envio, vocÃª pode enviar diretamente para o nosso email em: teste@teste.com</p>";
  }
}

function enviarFormulario(event) {
  event.preventDefault();
  const botao = document.querySelector("form button");
  botao.disabled = true;
  botao.innerText = "Enviando...";

  const data = new FormData(formulario);

  fetch("./enviar.php", {
    method: "POST",
    body: data,
  });
}

formulario.addEventListener("submit", enviarFormulario);
